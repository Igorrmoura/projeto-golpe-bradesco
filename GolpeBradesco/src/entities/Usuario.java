package entities;

public class Usuario {
	
	private String login;
	private int senha;
	
	
	public Usuario(String login, double senha) {
	
	}


	public Usuario() {
		
	}


	public String getLogin() {
		return login;
	}


	public void setLogin(String login) {
		this.login = login;
	}


	public int getSenha() {
		return senha;
	}


	public void setSenha(int senha) {
		this.senha = senha;
	}


		
}



